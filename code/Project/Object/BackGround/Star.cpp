//========================================
// 更新処理
// Author:KOMURO HIROMU
//========================================
void CStar::Update(void) {

	m_moveCounter++;	// カウンターを進める


	if (m_moveCounter % s_maxAnimeCounter == 0)
	{
		m_move = vecRand() * 0.05f;
		m_move.z = 0.0f;
	}

	// 移動量を位置に増加
	m_pos += m_move;
	m_rot += s_moveRot;

	if (m_posOld.x + s_PosOver <= m_pos.x || m_posOld.x - s_PosOver >= m_pos.x)
	{
		m_move *= -1;
	}

	//RNLib::Polygon3D()->Put;

		
}

